const runner = async function () {
    let res = await axios.get("https://en.wikipedia.org/wiki/List_of_ISO_639-1_codes");
    const $ = load(res.data);
    let table = $("#Table").first();
    let headers = {};
    table.find(">thead>tr, tbody>tr:has(th)").first().children().each(function (i) {
        let title = $(this);
        let label = title.text().replace(/\s+/g, " ").trim();
        label && (headers[i] = label);
    });
    console.log(JSON.stringify(headers, null, 4));
    // console.log(res.data);
    let rows = [];
    table.find(">tbody>tr:has(td)").each(function (i) {
        let cells = $(this).children();
        let doc = {};
        cells.each(function (j) {
            let td = $(this);
            let label = headers[j];
            let value = td.text().replace(/\s+/g, " ").trim();
            doc[label] = value;
        });
        //do something with doc
        rows.push(doc);
    });
    // console.log(JSON.stringify(rows, null, 4));
    //convert array to object with 639-1 and 639-2/T as keys
    let obj = {};
    rows.forEach(function (row) {
        if (obj[row["639-1"]] || obj[row["639-2/T"]]) {
            throw new Error("duplicate key " + (obj[row["639-1"]] || obj[row["639-2/T"]]));
        }
        obj[row["639-1"]] = row["ISO language name"];
        obj[row["639-2/T"]] = row["ISO language name"];
    });
    console.log(JSON.stringify(obj, null, 4));
};

runner();


